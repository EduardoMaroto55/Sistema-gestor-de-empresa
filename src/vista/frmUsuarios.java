    private void tableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUsuariosMouseClicked
        // TODO add your handling code here:
        try {
            int fila = tableUsuarios.getSelectedRow();
            txtIdUsuario.setText(tableUsuarios.getValueAt(fila, 0).toString());
            txtNombre.setText(tableUsuarios.getValueAt(fila, 1).toString());
            txtApe1.setText(tableUsuarios.getValueAt(fila, 2).toString());
            txtApe2.setText(tableUsuarios.getValueAt(fila, 3).toString());
            txtEmail.setText(tableUsuarios.getValueAt(fila, 4).toString());
            txtNombreUsuario.setText(tableUsuarios.getValueAt(fila, 5).toString());
            txtPassword.setText(tableUsuarios.getValueAt(fila, 6).toString());
            cbTipoUsuario.setSelectedItem(tableUsuarios.getValueAt(fila, 7));
            String Activo = tableUsuarios.getValueAt(fila, 8).toString();
            if ("TRUE".equals(Activo)) {
                chbSiNo.setSelected(true);
            } else {
                chbSiNo.setSelected(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al seleccionar un registro: " + e.getMessage());
        }
    }//GEN-LAST:event_tableUsuariosMouseClicked

